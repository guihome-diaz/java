package eu.daxiongmao.prv.astrology.model.chinese;

/**
 * This class represents the type of year (YIN - YANG).<br>
 * In Chinese culture all YIN has a YANG equivalent such as the moon (YIN) and the sun (YANG).<br>
 * 
 * @author Guillaume Diaz
 * @author Wikipedia (https://en.wikipedia.org/wiki/Chinese_astrology) - This is were the data comes from
 * @version 1.0 - 2016/12
 *
 */
public enum ChineseType {

    /** YIN (feminine, soft). ex: moon */
    YIN,

    /** YANG (masculine, hard). ex: sun. */
    YANG;

}
